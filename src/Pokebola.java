public abstract class Pokebola {

     private String esferica;
     private String metade_inferior = "branca";
     private String botao_central;

    public Pokebola(String esferica, String metade_inferior, String botao_central) {
        this.esferica = esferica;
        this.metade_inferior = metade_inferior;
        this.botao_central = botao_central;
    }

}
